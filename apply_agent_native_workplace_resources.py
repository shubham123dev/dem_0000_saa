def run(repo: Path, *args: str) -> str:
    completed = subprocess.run(
        [*args],
        cwd=repo,
        capture_output=True,
        text=True,
        check=False,
    )
    if completed.returncode != 0:
        raise RuntimeError(
            f"Command failed: {' '.join(args)}\n{completed.stdout}{completed.stderr}"
        )
    return completed.stdout.strip()


def verify_checkout(repo: Path) -> None:
    if not (repo / ".git").exists():
        raise RuntimeError("--repo must point to the repository root")
    branch = run(repo, "git", "branch", "--show-current")
    if branch != EXPECTED_BRANCH:
        raise RuntimeError(f"Expected branch {EXPECTED_BRANCH}, found {branch}")
    commit = run(repo, "git", "rev-parse", "HEAD")
    if commit != EXPECTED_COMMIT:
        raise RuntimeError(
            f"Expected commit {EXPECTED_COMMIT}, found {commit}. "
            "Fetch/pull the exact baseline or rebuild the patch for the newer commit."
        )
    dirty = run(repo, "git", "status", "--porcelain", "--untracked-files=no")
    if dirty:
        raise RuntimeError(
            "Tracked or staged changes are present. Commit or restore them before applying."
        )
    remote = run(repo, "git", "remote", "get-url", "origin")
    normalized = remote.lower().replace("\\", "/")
    if "shubham123dev/dem_0000_saa" not in normalized:
        raise RuntimeError(f"Unexpected origin remote: {remote}")


def replace_once(path: Path, old: str, new: str) -> None:
    text = path.read_text(encoding="utf-8")
    count = text.count(old)
    if count != 1:
        raise RuntimeError(
            f"Expected exactly one source block in {path}, found {count}"
        )
    path.write_text(text.replace(old, new, 1), encoding="utf-8")


def append_once(path: Path, addition: str) -> None:
    text = path.read_text(encoding="utf-8")
    marker = addition.strip().splitlines()[0]
    if marker in text:
        raise RuntimeError(f"Patch content already appears in {path}")
    path.write_text(text.rstrip() + "\n" + addition.strip() + "\n", encoding="utf-8")


def validate_result(repo: Path) -> None:
    run(repo, sys.executable, "-m", "compileall", "-q", "app", "tests")
    run(repo, "git", "diff", "--check")
    tool_registry = (repo / "app/agent/tool_registry.py").read_text(encoding="utf-8")
    for name in (
        "list_workplace_resource_types",
        "describe_workplace_resource",
        "search_workplace_resources",
        "get_workplace_resource",
        "count_workplace_resources",
    ):
        if name not in tool_registry:
            raise RuntimeError(f"Missing read tool after patch: {name}")
    action_registry = (repo / "app/agent/action_registry.py").read_text(encoding="utf-8")
    if action_registry.count("name=\"") < 38:
        raise RuntimeError("Existing action registry appears incomplete")
    catalog_text = (repo / "app/workplace_resources/registry.py").read_text(
        encoding="utf-8"
    ).lower()
    if '_field("password"' in catalog_text:
        raise RuntimeError("Password entered the workplace resource registry")


def apply(repo: Path) -> None:
    touched = sorted(set(FILES_TO_WRITE) | set(REPLACEMENTS) | set(APPENDS))
    backup_root = Path(tempfile.mkdtemp(prefix="workplace_agent_native_backup_"))
    existed: dict[str, bool] = {}
    try:
        for relative in touched:
            source = repo / relative
            existed[relative] = source.exists()
            if source.exists():
                backup = backup_root / relative
                backup.parent.mkdir(parents=True, exist_ok=True)
                shutil.copy2(source, backup)

        for relative, content in FILES_TO_WRITE.items():
            target = repo / relative
            if not existed.get(relative, False) and target.exists():
                raise RuntimeError(f"Refusing to overwrite unexpected new file: {relative}")
            target.parent.mkdir(parents=True, exist_ok=True)
            target.write_text(content, encoding="utf-8")

        for relative, pairs in REPLACEMENTS.items():
            target = repo / relative
            if not target.exists():
                raise RuntimeError(f"Required source file is missing: {relative}")
            for old, new in pairs:
                replace_once(target, old, new)

        for relative, addition in APPENDS.items():
            target = repo / relative
            if not target.exists():
                raise RuntimeError(f"Required append target is missing: {relative}")
            append_once(target, addition)

        validate_result(repo)
    except Exception:
        for relative in reversed(touched):
            target = repo / relative
            backup = backup_root / relative
            if existed.get(relative, False):
                target.parent.mkdir(parents=True, exist_ok=True)
                shutil.copy2(backup, target)
            elif target.exists():
                target.unlink()
        raise
    finally:
        shutil.rmtree(backup_root, ignore_errors=True)


